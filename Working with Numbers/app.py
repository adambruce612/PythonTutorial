from math import * #imports all from math module
 
print(2.0987) #can be a floating point or int
print(3 * 4 + 5) #pemdas can be applied
print(9 % 3)
my_num = 5
print(str(my_num)) #converts number to a string
print(str(my_num) + " my favorite number")
print(abs(my_num)) #absolute value of a number
print(pow(my_num, 5)) #first number is base, second is power
print(max(10, 18)) #returns the larger of the two numbers passed in
print(min(10, 15)) #returns the smaller of the two numbers
print(round(3.2)) #rounds number based on typical rounding conventions

#part of math class
print(floor(3.2)) #always rounds down
print(ceil(3.2)) #always rounds up
print(sqrt(36)) #returns square root